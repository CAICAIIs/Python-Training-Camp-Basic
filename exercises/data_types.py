def get_data_types():
    """
    创建并返回不同数据类型的变量
    
    返回:
    - 包含不同数据类型的元组: (整数, 浮点数, 字符串, 布尔值)
    """
    # 创建整数变量
    integer_var = 42
    
    # 创建浮点数变量
    float_var = 3.14
    
    # 创建字符串变量
    string_var = "Python编程"
    
    # 创建布尔值变量
    bool_var = True
    
    # 返回包含所有变量的元组
    return (integer_var, float_var, string_var, bool_var) 